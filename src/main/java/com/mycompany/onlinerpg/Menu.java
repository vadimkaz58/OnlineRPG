    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        if (!jTextField7.getText().equals("") && !jTextField3.getText().equals("")) {
            try {
                FileWriter writer = new FileWriter("servers.txt", true);
                BufferedWriter bufferWriter = new BufferedWriter(writer);
                bufferWriter.write("\n" + jTextField7.getText() + " " + jTextField3.getText() + " " + "\"" + String.valueOf(jPasswordField3.getPassword()) + "\"");
                bufferWriter.close();
            }
            catch (Exception e) {
                System.out.println(e);
            }
            listModel.addElement(jTextField7.getText());
            serversName.add(jTextField7.getText());
            serversIp.add(jTextField3.getText());
            serversPass.add("\"" + String.valueOf(jPasswordField3.getPassword()) + "\"");
            jTabbedPane1.setSelectedIndex(4);
            jLabel8.setForeground(Color.black);
            jLabel7.setForeground(Color.black);
        } else {
            jLabel8.setForeground(Color.red);
            jLabel7.setForeground(Color.red);
        }
    }//GEN-LAST:event_jButton20ActionPerformed

