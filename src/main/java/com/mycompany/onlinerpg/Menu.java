    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        try {
            int index = jList1.getSelectedIndex();
            listModel.remove(index);
            serversName.remove(index);
            serversIp.remove(index);
            serversPass.remove(index);
            Formatter formatter = new Formatter("servers.txt");
            int size = serversName.size();
            for (int i = 0; i < size; i++) {
               if (size != i + 1) { 
                   formatter.format("%s %s %s\n", serversName.get(i), serversIp.get(i), serversPass.get(i));
               } else {
                   formatter.format("%s %s %s", serversName.get(i), serversIp.get(i), serversPass.get(i));
               }
            }
            formatter.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton17ActionPerformed

