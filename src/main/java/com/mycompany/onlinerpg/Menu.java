    private void acceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptActionPerformed
        if (!jToggleButton1.isSelected()) {
            this.setSize(dimension);
        }
        try {
            Formatter formatter = new Formatter("config.ini");
            formatter.format("IsFullScreen=" + " ");
            if (jToggleButton1.isSelected()) {
                formatter.format("true" + "\n");
            } else {
                formatter.format("false" + "\n");
            }
            formatter.format("ScreenExpansion=" + " " + dimension.width + " " + dimension.height + " " + jComboBox1.getSelectedIndex() + "\n");
            formatter.close();
        } catch(Exception e) {
            System.out.println(e);
        }
        jTabbedPane1.setSelectedIndex(0);
    }//GEN-LAST:event_acceptActionPerformed

